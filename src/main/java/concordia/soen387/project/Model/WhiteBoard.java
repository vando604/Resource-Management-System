package concordia.soen387.project.Model;


public class WhiteBoard {
	
	private int height;
	private int width;
	private int id;
	
	public WhiteBoard(){}
	

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
	

	

}
